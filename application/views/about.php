                            </li>
                            <li>
                                2.  Menggali potensi daerah melalui pemetaan bentuk-bentuk kesenian dan ekspresi budaya yang mengakar dan berkembang di wilayah setempat
                            </li>
                            <li>
                                3.  Menciptakan nilai, makna, karakter, dan identitas yang terkait dengan potensi seni budaya setempat
                            </li>
                            <li>
                                4.  Mendorong kreativitas berkesenian 
                            </li>
                            <li>
                                5.  Meningkatkan kesadaran masyarakat untuk mengapresiasi seni
                            </li>
                            <li>
                                6.  Mendorong masyarakat untuk melakukan proses dokumentasi, konservasi dan preservasi bentuk kesenian daerah
                            </li>
                            <li>
                                7.  Mendekatkan masyarakat dengan kegiatan kesenian     
                            </li>
                            <li>
                                8.  Membangun jejaring antar seniman
                            </li>
                        </ul>
                    </p>
